// Default icon getter
export const getSpecialtyIcon = (specialty: string, className?: string) => {
    const lowerSpecialty = specialty.toLowerCase();
    if (lowerSpecialty.includes('cardio')) return <HeartIcon className={className} />;
    if (lowerSpecialty.includes('neuro')) return <BrainIcon className={className} />;
    if (lowerSpecialty.includes('ortho')) return <BoneIcon className={className} />;
    if (lowerSpecialty.includes('derma')) return <SkinIcon className={className} />;
    if (lowerSpecialty.includes('gastro')) return <StomachIcon className={className} />;
    if (lowerSpecialty.includes('pulmo')) return <LungsIcon className={className} />;
    if (lowerSpecialty.includes('onco')) return <CancerRibbonIcon className={className} />;
    if (lowerSpecialty.includes('endo')) return <HormoneIcon className={className} />;
    return <MedicalIcon className={className} />;
};
